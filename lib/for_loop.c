#include <stdio.h>


// for loop = repeats a section of code a limited amount of times


int main() {

    int i;

    /*
        1st statement = starting from
        2nd statement = arriving to
        3rd stament = whats the process between
    */

    for(i = 1; i <= 10; i++) {
        printf("%d\n", i);
    }

    return 0;
}